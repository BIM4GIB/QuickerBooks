// src/auth/oauth.ts
var AUTH_URL = "https://appcenter.intuit.com/connect/oauth2";
var TOKEN_URL = "https://oauth.platform.intuit.com/oauth2/v1/tokens/bearer";
var SCOPE = "com.intuit.quickbooks.accounting";
var CALLBACK_TIMEOUT_MS = 5 * 60 * 1e3;
function basicAuth(clientId, clientSecret) {
  return Buffer.from(`${clientId}:${clientSecret}`).toString("base64");
}
function generateCodeVerifier() {
  return randomBytes(32).toString("base64url");
}
function generateCodeChallenge(verifier) {
  return createHash("sha256").update(verifier).digest("base64url");
}
function buildAuthUrl(config) {
  const state = randomBytes(16).toString("hex");
  const codeVerifier = generateCodeVerifier();
  const codeChallenge = generateCodeChallenge(codeVerifier);
  const params = new URLSearchParams({
    client_id: config.clientId,
    redirect_uri: config.redirectUri,
    response_type: "code",
    scope: SCOPE,
    state,
    code_challenge: codeChallenge,
    code_challenge_method: "S256"
  });
  return { url: `${AUTH_URL}?${params}`, state, codeVerifier };
}
async function exchangeCode(code, realmId, config, codeVerifier) {
  const res = await fetch(TOKEN_URL, {
    method: "POST",
    headers: {
      "Content-Type": "application/x-www-form-urlencoded",
      Accept: "application/json",
      Authorization: `Basic ${basicAuth(config.clientId, config.clientSecret)}`
    },
    body: new URLSearchParams({
      grant_type: "authorization_code",
      code,
      redirect_uri: config.redirectUri,
      code_verifier: codeVerifier
    })
  });
  if (!res.ok) {
    throw new Error(`Token exchange failed (HTTP ${res.status}). Check your app credentials.`);
  }
  const body = await res.json();
  const now = Date.now();
  return {
    access_token: body.access_token,
    refresh_token: body.refresh_token,
    token_type: body.token_type,
    expires_at: now + body.expires_in * 1e3,
    refresh_expires_at: now + body.x_refresh_token_expires_in * 1e3,
    realm_id: realmId
  };
}
function startOAuthCallbackServer(config, expectedState, codeVerifier) {
  return new Promise((resolve, reject) => {
    const redirectUrl = new URL2(config.redirectUri);
    const port = parseInt(redirectUrl.port || "9876", 10);
    const server = createServer(async (req, res) => {
      try {
        const url = new URL2(req.url || "/", `http://localhost:${port}`);
        if (url.pathname !== redirectUrl.pathname) {
          res.writeHead(404, { "Content-Type": "text/plain" });
          res.end("Not found");
          return;
        }
        const code = url.searchParams.get("code");
        const state = url.searchParams.get("state");
        const realmId = url.searchParams.get("realmId");
        const error = url.searchParams.get("error");
        if (error) {
          res.writeHead(400, { "Content-Type": "text/plain" });
          res.end(`Authorization failed: ${error}`);
          cleanup();
          reject(new Error(`OAuth error: ${error}`));
          return;
        }
        if (!code || !realmId) {
          res.writeHead(400, { "Content-Type": "text/plain" });
          res.end("Missing code or realmId");
          cleanup();
          reject(new Error("Missing code or realmId in callback"));
          return;
        }
        if (state !== expectedState) {
          res.writeHead(400, { "Content-Type": "text/plain" });
          res.end("State mismatch \u2014 possible CSRF. Please retry.");
          cleanup();
          reject(new Error("OAuth state mismatch"));
          return;
        }
        const tokens = await exchangeCode(code, realmId, config, codeVerifier);
        await saveTokens(tokens);
        res.writeHead(200, { "Content-Type": "text/html" });
        res.end(
          "<h1>Authorization successful!</h1><p>You can close this window and return to the terminal.</p>"
        );
        cleanup();
        resolve(tokens);
      } catch (err) {
        res.writeHead(500, { "Content-Type": "text/plain" });
        res.end("Authorization failed. Check the terminal for details.");
        cleanup();
        reject(err);
      }
    });
    const timeout = setTimeout(() => {
      cleanup();
      reject(new Error("OAuth callback timed out after 5 minutes. Please try again."));
    }, CALLBACK_TIMEOUT_MS);
    function cleanup() {
      clearTimeout(timeout);
      server.close();
    }
    server.listen(port, "127.0.0.1", () => {
      console.error(`OAuth callback server listening on 127.0.0.1:${port}`);
    });
    server.on("error", (err) => {
      clearTimeout(timeout);
      reject(err);
    });
  });
}

